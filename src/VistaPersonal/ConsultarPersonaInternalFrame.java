      private int ventana() {
        int seleccion = JOptionPane.showOptionDialog(
                rootPane,
                "desea eliminar la persona ",
                "Selector de opciones ",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.CANCEL_OPTION,
                null,
                new Object[]{" Si", "Cancelar"},
                "opcion 1");
        return seleccion;
    }
      
      public void consultarTodasPersonas(){
        ArrayList<PersonaDto> lista = new ArrayList();
       
        this.tabla.setRowCount(0);
        this.tabla.fireTableDataChanged();
       lista=this.principal.getFachada().listarPersonas();

     this.adicionarATabla(lista);
      }
      
    public void consultarPersona() {
        ArrayList<PersonaDto> lista = new ArrayList();

        if (this.txtConsultar.getText().equals("")) {
            return;
        }

        int valor = Integer.parseInt(this.txtConsultar.getText());
        this.tabla.setRowCount(0);
        this.tabla.fireTableDataChanged();
        if (this.OptCedula.isSelected()) {
            lista = this.principal.getFachada().listarPersonaXCedula(valor);
        }
        if (this.OptCodigo.isSelected()) {
            lista = this.principal.getFachada().listarPersonaXCodigo(valor);
        }

     this.adicionarATabla(lista);

    }
    
    private void adicionarATabla(ArrayList<PersonaDto> lista){
       String registro[] = new String[10];
        for (PersonaDto dto : lista) {
            registro[0] = dto.getNuip() + "";
            registro[1] = dto.getCodigo() + "";
            registro[2] = dto.getNombre() + "";
            registro[3] = dto.getApellido() + "";
            registro[4] = dto.getDireccion() + "";
            registro[5] = dto.getTelefono() + "";
            registro[6] = this.acomodarTipo(dto.getTipo()) + "";
            registro[7] = this.acomodarActivo(dto.getActivo()) + "";
            registro[8] = dto.getPorcentaje() + "";
            registro[9]=dto.getNacionalidad()+"";

            this.tabla.addRow(registro);
            this.tabla.fireTableDataChanged();

        }
    }
    
     private String acomodarTipo(String tipo){
    if(tipo.equals("V")){
    return "Vendedor";
    }
    if(tipo.equals("P")){
    return "Promotor";
    }
    if(tipo.equals("A")){
    return "Administrativo";
    }
    return null;
    }
    
    
    private String acomodarActivo(String activo){
          if(activo.equals("S")){
    return "Si";
    }
    if(activo.equals("N")){
    return "No";
    }
    return null;
    }

