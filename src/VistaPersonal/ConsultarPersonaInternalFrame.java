    private void tableConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableConsultaMouseClicked
        // TODO add your handling code here:
            if (this.tableConsulta.getSelectedRow() >= 0) {

            int seleccion = this.ventana();
            if (seleccion == 0) {
               
                int cedula = Integer.parseInt(this.tableConsulta.getValueAt(this.tableConsulta.getSelectedRow(), 0).toString());
                
                boolean valor = this.principal.getFachada().eliminarPersona(cedula);
                if (valor) {
                    
                    this.tabla.removeRow(this.tableConsulta.getSelectedRow());

                } else {
                    JOptionPane.showMessageDialog(null, "existe un error al eliminar persona debido a que esta ya tiene\n"
                            + "registrado ventas");

                }
            }
        }
    }//GEN-LAST:event_tableConsultaMouseClicked

