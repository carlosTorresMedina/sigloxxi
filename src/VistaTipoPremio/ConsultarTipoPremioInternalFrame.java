    public void consultarTodo(){
     ArrayList<TipoPremioDto> lista = new ArrayList();

       
        this.tabla.setRowCount(0);
        this.tabla.fireTableDataChanged();

        lista = this.principal.getFachada().listarTipoPremios();

        this.adicionarATabla(lista);
    }
    
    private void consultar() {
        ArrayList<TipoPremioDto> lista = new ArrayList();

        if (this.txtConsultar.getText().equals("")) {
            return;
        }
        this.tabla.setRowCount(0);
        this.tabla.fireTableDataChanged();

        lista = this.principal.getFachada().listarTipoPremioXTipo(Integer.parseInt(this.txtConsultar.getText()));

        this.adicionarATabla(lista);
    }

    private void adicionarATabla(ArrayList<TipoPremioDto> lista) {
        String registro[] = new String[4];

        for (TipoPremioDto dto : lista) {

            registro[0] = dto.getTipo() + "";
            registro[1] = dto.getNombre() + "";
            registro[2] = dto.getValorPesosFormato() + "";
            registro[3] = dto.getValorBolivaresFormato() + "";
            this.tabla.addRow(registro);
            this.tabla.fireTableDataChanged();

        }
    }

