    private void tableConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableConsultaMouseClicked
        // TODO add your handling code here:
        if (this.tableConsulta.getSelectedRow() >= 0) {

            int seleccion = this.ventana();
            if (seleccion == 0) {

                int tipo = Integer.parseInt(this.tableConsulta.getValueAt(this.tableConsulta.getSelectedRow(), 0).toString());
                boolean valor = this.principal.getFachada().eliminarTipoPremio(tipo);
                if (valor) {

                    this.tabla.removeRow(this.tableConsulta.getSelectedRow());

                } else {
                    JOptionPane.showMessageDialog(null, "existe un error al eliminar el tipo premio  ya que este tiene\n"
                        + "premios asociados a el");

                }
            }
        }
    }//GEN-LAST:event_tableConsultaMouseClicked

     private int ventana() {
        int seleccion = JOptionPane.showOptionDialog(
                rootPane,
                "desea eliminar el tipo premio ",
                "Selector de opciones ",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                new Object[]{" si", "Cancelar"},
                "opcion 1");
        return seleccion;
    }
     
    
