    /**
     * retorna verdadero si existe un campo vacio y falso si todos estan llenos
     *
     * @return
     */
    private boolean validarCampos() {
        return (this.txtTipoPremio.getText().isEmpty() || this.txtNombre.getText().isEmpty() || this.txtValorBolivares.getText().isEmpty() || this.txtValorPesos.getText().isEmpty());
    }

    private void registrar() {
        if (!this.validarCampos()) {

            TipoPremioDto dto = new TipoPremioDto();
            dto.setTipo(Integer.parseInt(this.txtTipoPremio.getText()));
            dto.setNombre(this.txtNombre.getText());
            String bolivares = this.txtValorBolivares.getText().replace(".", "");
            String pesos = this.txtValorPesos.getText().replace(".", "");
            dto.setValorBolivares(Integer.parseInt(bolivares));
            dto.setValorPesos(Integer.parseInt(pesos));
            boolean valor = this.principal.getFachada().registrarTipoPremio(dto);
            if (valor) {
                JOptionPane.showMessageDialog(null, "el tipo premio se registro exitosamente");
                this.deshabilitarCampos();
                this.vaciarCampos();
                return;
            }
            JOptionPane.showMessageDialog(null, "el tipo premio ya se encuentra registrado");
        } else {
            JOptionPane.showMessageDialog(null, "No puede dejar campos vacios");
        }
    }

