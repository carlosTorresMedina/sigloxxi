    private void txtTipoPremioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTipoPremioKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            ArrayList<TipoPremioDto> lista = this.principal.getFachada().listarTipoPremioXTipo(Integer.parseInt(this.txtTipoPremio.getText()));
            if (!lista.isEmpty()) {
                int tipo = Integer.parseInt(this.txtTipoPremio.getText());

                for (TipoPremioDto p : lista) {
                    this.tipoAnterior = p.getTipo();

                    this.txtTipoPremio.setText(p.getTipo() + "");
                    this.txtNombre.setText(p.getNombre() + "");
                    this.txtValorBolivares.setText(p.getValorBolivaresFormato() + "");
                    this.txtValorPesos.setText(p.getValorPesosFormato() + "");
                }
                this.deshabilitarCampos();
                this.cmdHabilitar.setEnabled(true);
            } else {
                this.vaciarCamposBusqueda();
                this.cmdHabilitar.setEnabled(false);
                this.cmdModificar.setEnabled(false);
                this.habilitarCampos();
                this.txtNombre.requestFocus();
            }
        }
    }//GEN-LAST:event_txtTipoPremioKeyPressed

